function createEducationItem(item) {
  const article = document.createElement("article");
  article.className = "education-item with-logo timeline-item";
  article.setAttribute("role", "listitem");

  const img = document.createElement("img");
  img.className = "edu-logo";
  img.src = item.logo;
  img.alt = item.alt;

  const body = document.createElement("div");

  const title = document.createElement("h3");
  title.textContent = item.institution;

  const credential = document.createElement("p");
  credential.className = "credential";
  credential.textContent = item.credential;

  const period = document.createElement("p");
  period.className = "period";
  period.textContent = item.period;

  body.appendChild(title);
  body.appendChild(credential);
  body.appendChild(period);

  if (Array.isArray(item.meta)) {
    item.meta.forEach((metaLine) => {
      const meta = document.createElement("p");
      meta.className = "meta";
      meta.textContent = metaLine;
      body.appendChild(meta);
    });
  }

  article.appendChild(img);
  article.appendChild(body);

  return article;
}

function renderEducationGroup(groupName, container) {
  const items = educationData[groupName] || [];
  const fragment = document.createDocumentFragment();

  items.forEach((item) => {
    fragment.appendChild(createEducationItem(item));
  });

  container.replaceChildren(fragment);
}

document.querySelectorAll("[data-education-group]").forEach((container) => {
  renderEducationGroup(container.dataset.educationGroup, container);
});
